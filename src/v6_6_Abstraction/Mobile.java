package v6_6_Abstraction;

// Abstraction means Hiding an implementation details from user and only provided functionality.
// main think is abstract is details are hidden in subclass.
/*
    Abstraction has two ways
    1) abstract classes and methods
    2) interfaces

    In abstract Class we can not create an Objects to instantiate.
     and in Abstract method we can not execute any details.
 */
public abstract class Mobile {

    public abstract void doubletap();
}
